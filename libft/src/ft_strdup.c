#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*c;

	i = ft_strlen(s1);
	if (!(c = (char *)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	c[i] = '\0';
	while (i >= 0)
	{
		c[i] = s1[i];
		i--;
	}
	return (c);
}
