#include "libft.h"

int		ft_get_number(const char *c, size_t *i)
{
	long long	number;

	number = 0;
	while (c[*i] == 32 || (c[*i] >= 9 && c[*i] <= 13))
		(*i)++;
	while (c[*i] >= '0' && c[*i] <= '9')
	{
		number = number * 10 + c[*i] - '0';
		(*i)++;
	}
	return ((int)number);
}
