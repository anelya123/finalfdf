#include "libft.h"

int		ft_strchri(const char *s, int c)
{
	int i;

	i = 0;
	if (c == 0)
		return (ft_strlen(s));
	while (s[i])
	{
		if (*s == c)
			return (i);
		i++;
	}
	return (-1);
}
